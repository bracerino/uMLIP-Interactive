def _stick_xy(peak_x, peak_y):
    """Build x/y arrays that draw each peak as a vertical line from 0 to its
    intensity, using None separators so one Scatter trace renders all sticks."""
    xs, ys = [], []
    for px, py in zip(peak_x, peak_y):
        xs.extend([px, px, None])
        ys.extend([0.0, py, None])
    return xs, ys


