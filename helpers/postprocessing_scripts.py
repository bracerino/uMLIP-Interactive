def render_average_structure_viewer():
    """Tab 2: upload an averaged structure file and inspect it (lattice
    parameters, formula, 3-D view)."""
    st.markdown("### 🧊 Inspect an averaged structure")
    st.caption(
        "Upload an `average_structure.*` produced by the generated script "
        "(`.cif`, `.vasp`/`POSCAR`, or extended `.xyz`) to view its averaged "
        "lattice parameters and a 3-D rendering."
    )

    upload = st.file_uploader(
        "Upload an averaged structure file",
        type=["cif", "vasp", "xyz", "poscar"],
        key="avg_struct_upload",
    )
    if upload is None:
        return

    import tempfile
    from ase.io import read as ase_read

    suffix = os.path.splitext(upload.name)[1] or ".xyz"
    try:
        with tempfile.NamedTemporaryFile(
            mode="wb", suffix=suffix, delete=False
        ) as tmp:
            tmp.write(upload.getbuffer())
            tmp_path = tmp.name
        atoms = ase_read(tmp_path)
    except Exception as exc:  # noqa: BLE001
        st.error(f"Could not read the structure file: {exc}")
        return
    finally:
        try:
            os.unlink(tmp_path)
        except (OSError, NameError):
            pass

    cellpar = atoms.cell.cellpar()
    info_cols = st.columns(4)
    info_cols[0].metric("Atoms", f"{len(atoms)}")
    info_cols[1].metric("Formula", atoms.get_chemical_formula("hill"))
    info_cols[2].metric("Volume (Å³)", f"{atoms.get_volume():.2f}")
    info_cols[3].metric("Cell rank", f"{atoms.cell.rank}")

    lat_cols = st.columns(6)
    for col, lab, val in zip(
        lat_cols,
        ["a (Å)", "b (Å)", "c (Å)", "α (°)", "β (°)", "γ (°)"],
        cellpar,
    ):
        col.metric(lab, f"{val:.4f}")

    try:
        from pymatgen.io.ase import AseAtomsAdaptor
        from helpers.structure_preview import view_structure

        structure = AseAtomsAdaptor().get_structure(atoms)
        st.iframe(view_structure(structure, height=420, width=620), height=440)
    except Exception as exc:  # noqa: BLE001
        st.info(f"3-D preview unavailable ({exc}). Lattice data shown above.")


